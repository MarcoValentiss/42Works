#include <stdio.h>

char    *strchr(const char *s, int c)
{
    size_t  i;
    char    a;

    a = (char)c;
    i = 0;
    while (s[i])
    {
        if (s[i] == a)
            return (s[i]);
        i++;
    }
    return (0);
}
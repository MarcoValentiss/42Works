int	ft_sqrt(int nb)
{
	long	i;

	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 2;
	while (nb >= i * i)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	s;

	i = 0;
	s = 0;
	if (to_find[s] == '\0')
	{
		return (str);
	}
	while (str[i] != '\0')
	{
		s = 0;
		while (str[i + s] == to_find[s] && str[i + s] != '\0')
		{
			if (to_find[s + 1] == '\0')
			{
				return (&str[i]);
			}
			s++;
		}
		i++;
	}
	return (0);
}
